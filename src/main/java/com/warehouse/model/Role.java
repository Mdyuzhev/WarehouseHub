package com.warehouse.model;

/**
 * Роли пользователей системы
 * 
 * SUPER_USER - полный доступ ко всем функциям
 * ADMIN - управление пользователями, просмотр статуса системы
 * MANAGER - просмотр отчётности и списка товаров (только чтение)
 * EMPLOYEE - работа с товарами (добавление, удаление)
 */
public enum Role {
    SUPER_USER,  // Суперпользователь - полные права
    ADMIN,       // Администратор - управление пользователями
    MANAGER,     // Менеджер - просмотр отчётов
    EMPLOYEE     // Сотрудник - работа с товарами
}
