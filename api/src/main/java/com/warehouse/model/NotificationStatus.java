package com.warehouse.model;

/**
 * Статусы обработки уведомления
 */
public enum NotificationStatus {
    /**
     * Ожидает отправки
     */
    PENDING,

    /**
     * В процессе отправки
     */
    SENDING,

    /**
     * Успешно отправлено
     */
    SENT,

    /**
     * Ошибка отправки (будет повторная попытка)
     */
    FAILED,

    /**
     * Финально провалено (превышен лимит повторов)
     */
    DEAD
}
